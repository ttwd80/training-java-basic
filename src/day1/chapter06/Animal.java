package day1.chapter06;

public class Animal {

    public void run() {
        System.out.println("Animal.run()");
    }

    void climb() {
        System.out.println("Animal.climb()");
    }

    protected void sleep() {
        System.out.println("Animal.sleep()");
    }

    private void rest() {
        System.out.println("Animal.rest()");
    }
}
